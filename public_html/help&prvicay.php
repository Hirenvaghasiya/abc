    <h2>
        <a name="SourceCode"></a>Source code Guidelines</h2>
    <p>
        First and foremost, does the code actually work? We do sometimes get code that,
        for one reason or another, just does not work. Please check that the source code
        you send compiles cleanly, and if part of a larger demo application, that the application
        itself runs OK.
    </p>
    <p>
        We do ask that the general conventions be adhered for your language to so that other
        programmers can understand your code easily. Conventions that help other programmers
        read your code are:
    </p>
    <ul>
        <li>The use of Hungarian notation in MFC (e.g. Variable prefixes such as "n" for int,
            "d" for double etc.)</li>
        <li>The use of Win32/Visual C++ types such as UINT, LPCTSTR etc.</li>
        <li>Camel case in .NET code</li>
    </ul>
    <p>
        Also, does the code compile cleanly under the warning level 4?</p>
    <h2>
        Sample Project<a name="Sample"></a></h2>
    <p>
        <b>Sample Executable:</b> If providing a sample executable, please make sure it
        is linked with the <b>release </b>libraries (ie. <b>Make sure it is not linked with
            the Unicode or debug libraries</b>). The whole point of a demo executable is
        to give developers a quick intro to your code. If they have to recompile in order
        to get the application to run on their system then there is no use in supplying
        the executable in the first place. Secondly try to conservative about just how big
        a sample you upload, a 4Mb sample just isn't going to interest a lot of people.</p>
    <p>
        <b>Sample Project:</b>It is recommended that you also include a sample project.
        When creating a zip file for the sample project, please do not include either the
        Debug or the Release directories. They simply inflate the size of the ZIP file.
        Also, do not include the *.clw, *.ncb, *.opt and other such files that are automatically
        recreated.
    </p>
    <h2>
        <a name="Other"></a>Submitting an article based on another person's work</h2>
    <p>
        It is OK to use others' code as a <span class="bhi52z19" id="bhi52z19_8">base</span> as long as you make it totally clear that
        some/most of the code isn't yours. <strong><b>If you use someone else's code then DO
        NOT remove their copyright notices</b>.</strong> There is a lot of code reuse
        here, that's the point after all. As far as the articles that are being posted here
        we expect to see credit where credit's due, in the code and the HTML as appropriate.</p>
    <p>
        If you are submitting a modification, extension or correction to another person's
        work then we ask that you attempt to contact the original author first and then
        try to liaise with him/her to produce an update to the original article. This way
        we can retain continuity with the original article as well as reducing the number
        of completely new pages that need posting.</p>
    <h2>
