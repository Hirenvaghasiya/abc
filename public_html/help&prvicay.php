    <h2>The Article<a name="Article"></a></h2>
    <p>
        First and foremost, <b>we need <i>some</i> documentation</b>. This doesn't have
        to be anything fancy, though we don't mind if it is, but if you think about answering
        the following questions then you should be off to a good start.
    </p>
    <ul>
        <li>What does the code do?</li>
        <li>How do I integrate it with my existing code or how do I use it?</li>
        <li>If there is a similar article on ask4u already, then how 
            does this one differ? Why would someone want to use your version?</li>
        <li>Is there some aspect of the code that is of particular interest that perhaps should
            be covered in the article?</li>
        <li>Are there any particular environments the code is restricted to? (Windows 2000 or
            above, .NET SP1 etc)</li>
    </ul>
    <p>
        The idea is to give the reader a clear idea of the purpose of your code, instead
        of forcing them to download a project, build it and then hunt around to find out
        what the sample does. This means articles that are code snippets, with no explanation 
        of what the snippet does, are insufficient. 
    </p>
    <p>
        The quickest way to get your code posted is to provide a simple HTML file. Our preferences
        on documentation are:</p>
    <ol>
        <li>A simple HTML edited by a plain text editor or good HTML editor, using the <u>template file</u> provided,</li>
        <li>A plain ol' text file,</li>
        <li>Everything else.</li>
    </ol>
    <p>All the articles at ask4u have the same look and feel which is 
        achieved by only the most basic HTML features. If you send us a HTML file with 
        different fonts and colours and fancy bits, chances are it will all be stripped 
        out to make it conform to the ask4u standard. All 
        documentation is edited <b>by hand</b>, so it can be a real nightmare
        wading through masses of convoluted HTML.
    </p>
    
