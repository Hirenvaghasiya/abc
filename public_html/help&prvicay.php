<h2><a name="intro"></a>Do you have some cool code you want to share?</h2>
<p>Sharing code with others is a <span class="bhi52z19" id="bhi52z19_4">great way</span> to not only help others, but also to become a better 
programmer. You'll get a lot of positive feedback, suggestions for improvements, <span class="bhi52z19" id="bhi52z19_3">bug fixes</span>, free 
testing, and more often than not, suggestions on how to make the code itself even better. It also 
encourages others to share their code, which in turn gives you access to an even larger base of free 
<span class="bhi52z19" id="bhi52z19_2">source code</span>.</p>

<h2><a name="how"></a>How it works</h2>
<p>You can submit either a full article with downloads and images, or just a <span class="bhi52z19" id="bhi52z19_5">quick Tip</span> n' Trick.
Tips n tricks are meant to be very, very short tips, snippets or even just a one-liner you came
across that saved your day. Articles are for presenting more detailed ideas.</p>
<ol>
<li>There are two ways to submit an article: via email to 
<a href="mailto:Submit@ask4u.com">Submit@ask4u.com</a> 
or via the Article Submission Wizard.</li>
<li>Articles submitted via the submission wizard will initially be in an "unedited" state. Articles
submitted via email will be posted in an "edited" state.</li>
<li>If your article is not yet edited then you can continue to modify your article via the Modify 
button on the article itself, or by sending us your changes via email at 
Submit@<span class="bhi52z19" id="bhi52z19_7">ask4u</span>.com - you choose.</li>
<li>If you post using the submission wizard then your article will go into a moderation queue where 
<span class="bhi52z19" id="bhi52z19_6">high level</span> members can review and approve your article. During moderation you can edit your article 
as often as necessary, and very soon (within a few hours) it will be approved for general viewing, 
unless there are issues with the article that need addressing.</li>
<li>An editor may then formally edit your article which involves reviewing your downloads, images,
text and formatting and polishing the entire article. Once this is done you may continue to send us
updates to your article as often as you like to Submit@ask4u.com, 
but direct editing is no longer possible since it may undo the editing work already done. Just let us 
know if you do not wish for your article to get this final polish.</li>
</ol>

