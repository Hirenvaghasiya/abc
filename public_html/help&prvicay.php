        <a name="Prepare"></a>Preparing your code</h2>
    <p>
        If you have some great code, but do not make it easy for others to use it, then
        more often than not people will pass it by and not use it. We suggest:</p>
    <ul>
        <li>Write a brief description of your code, including what it is, what it does, and
            how to use it in your projects (Download a 
            template file here, and read template instructions 
            here</li>
        <li>Explain any tricky problems that you solved while developing the code. It makes
            great reading and helps others solve similar problems.</li>
        <li>Comment your code</li>
        <li>Include a <b>small</b> screen shot</li>
        <li>Prepare a <b>small</b> app to demonstrate the code (only if appropriate)</li>
    </ul>
    <h2><a name="Submit"></a>Submitting your code</h2>
    <p>
        When submitting your code and article please check you have the following:</p>
    <ul>
        <li><u>Article</u> explaining your code</li>
        <li>Small screen shot (PNG/GIF/JPEG only, maximum width 600 pixels)</li>
        <li><u>Zip file</u> containing your <u>source files</u>
            (if appropriate)</li>
        <li><u>Zip file</u> containing your <u>sample application</u>
            (if appropriate)</li>
        <li>Name and contact details</li>
    </ul>
    <p>
        Package all this up into a <b><u>single zip file</u> </b>and send it
        to <a href="mailto:Submit@ask4u.com">
            Submit@ask4u.com</a>.
    </p>
    <p>
        When emailing the article please state the title of the article in the subject line.
        Also if you think it neatly fits in one of the categories, please let us know.</p>
